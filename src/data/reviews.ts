// Deterministic picker — given a seed string and count, return that many reviews
// in a stable shuffled order. Used to spread different reviews across pages
// without repeating the same order on every page.
export function pickReviews(seed: string, count: number): Review[] {
  let h = 0;
  for (let i = 0; i < seed.length; i++) h = (h * 31 + seed.charCodeAt(i)) | 0;
  const arr = [...reviews];
  let s = Math.abs(h) || 1;
  const rand = () => {
    s = (s * 1664525 + 1013904223) >>> 0;
    return s / 0xffffffff;
  };
  for (let i = arr.length - 1; i > 0; i--) {
    const j = Math.floor(rand() * (i + 1));
    [arr[i], arr[j]] = [arr[j], arr[i]];
  }
  return arr.slice(0, count);
}
